function theBeatlesPlay(musicians, instruments) {
	var arr = []
	

	for (let i=0; i<musicians.length; i++) {
	  arr[i] = ""
		}

	for (let o=0; o<musicians.length; o++) {
	  arr[o] += musicians[o] + " plays " + instruments[o] 
	}
	
return arr
}

